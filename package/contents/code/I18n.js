function normalizeLanguage(localeName) {
    if (!localeName) {
        return "en"
    }

    return localeName.toString().split(/[_-]/)[0].toLowerCase()
}

function tr(localeName, text) {
    const language = normalizeLanguage(localeName)
    const catalog = translations[language] || {}
    return catalog[text] || text
}
